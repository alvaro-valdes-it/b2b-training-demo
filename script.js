const menuToggle = document.querySelector(".menu-toggle");
const navLinks = document.querySelector(".nav-links");

menuToggle.addEventListener("click", () => {
    navLinks.classList.toggle("active");

    if (navLinks.classList.contains("active")) {
        menuToggle.textContent = "×";
    } else {
        menuToggle.textContent = "☰";
    }
});

document.querySelectorAll(".nav-links a").forEach((link) => {
    link.addEventListener("click", () => {
        navLinks.classList.remove("active");
        menuToggle.textContent = "☰";
    });
});

const languageButtons = document.querySelectorAll(".lang-btn");
const translatableElements = document.querySelectorAll("[data-i18n]");
const languageLinks = document.querySelectorAll("[data-href-es][data-href-en]");

function setLanguage(language) {
    translatableElements.forEach((element) => {
        const key = element.getAttribute("data-i18n");

        if (translations[language][key]) {
            element.textContent = translations[language][key];
        }
    });

    languageLinks.forEach((link) => {
        const newHref = link.getAttribute(`data-href-${language}`);
        link.setAttribute("href", newHref);
    });

    languageButtons.forEach((button) => {
        button.classList.remove("active");

        if (button.getAttribute("data-lang") === language) {
            button.classList.add("active");
        }
    });

    document.documentElement.lang = language;
    localStorage.setItem("selectedLanguage", language);
}

languageButtons.forEach((button) => {
    button.addEventListener("click", () => {
        const language = button.getAttribute("data-lang");
        setLanguage(language);
    });
});

const savedLanguage = localStorage.getItem("selectedLanguage") || "en";
setLanguage(savedLanguage);

const galleryImages = document.querySelectorAll(".transformation-card img");

let currentImageIndex = 0;
let touchStartX = 0;
let touchEndX = 0;

const lightbox = document.createElement("div");
lightbox.classList.add("lightbox");

lightbox.innerHTML = `
    <button class="lightbox-close">&times;</button>
    <button class="lightbox-arrow lightbox-prev">&#10094;</button>
    <img class="lightbox-image" src="" alt="Enlarged image">
    <button class="lightbox-arrow lightbox-next">&#10095;</button>
`;

document.body.appendChild(lightbox);

const lightboxImage = document.querySelector(".lightbox-image");
const closeButton = document.querySelector(".lightbox-close");
const prevButton = document.querySelector(".lightbox-prev");
const nextButton = document.querySelector(".lightbox-next");

function openLightbox(index) {
    currentImageIndex = index;
    lightboxImage.src = galleryImages[currentImageIndex].src;
    lightbox.classList.add("active");
    document.body.style.overflow = "hidden";
}

function closeLightbox() {
    lightbox.classList.remove("active");
    document.body.style.overflow = "auto";
}

function showNextImage() {
    currentImageIndex++;

    if (currentImageIndex >= galleryImages.length) {
        currentImageIndex = 0;
    }

    lightboxImage.src = galleryImages[currentImageIndex].src;
}

function showPreviousImage() {
    currentImageIndex--;

    if (currentImageIndex < 0) {
        currentImageIndex = galleryImages.length - 1;
    }

    lightboxImage.src = galleryImages[currentImageIndex].src;
}

galleryImages.forEach((image, index) => {
    image.addEventListener("click", () => {
        openLightbox(index);
    });
});

closeButton.addEventListener("click", closeLightbox);
nextButton.addEventListener("click", showNextImage);
prevButton.addEventListener("click", showPreviousImage);

lightbox.addEventListener("click", (event) => {
    if (event.target === lightbox) {
        closeLightbox();
    }
});

document.addEventListener("keydown", (event) => {
    if (!lightbox.classList.contains("active")) return;

    if (event.key === "Escape") {
        closeLightbox();
    }

    if (event.key === "ArrowRight") {
        showNextImage();
    }

    if (event.key === "ArrowLeft") {
        showPreviousImage();
    }
});

lightbox.addEventListener("touchstart", (event) => {
    touchStartX = event.changedTouches[0].screenX;
});

lightbox.addEventListener("touchend", (event) => {
    touchEndX = event.changedTouches[0].screenX;
    handleSwipe();
});

function handleSwipe() {
    const swipeDistance = touchEndX - touchStartX;

    if (swipeDistance > 50) {
        showPreviousImage();
    }

    if (swipeDistance < -50) {
        showNextImage();
    }
}

const revealItems = document.querySelectorAll(
    "section h2, .section-intro, .service-card, .card, .transformation-card, .faq-container details, .about-photo, .about-text p, #contacto p"
);

revealItems.forEach((item) => {
    item.classList.add("reveal-on-scroll");
});

const revealObserver = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
        if (entry.isIntersecting) {
            entry.target.classList.add("visible");
        } else {
            entry.target.classList.remove("visible");
        }
    });
}, {
    threshold: 0.15
});

revealItems.forEach((item) => {
    revealObserver.observe(item);
});
